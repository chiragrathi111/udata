* LinkedHashMap and HashMap difference:-
Linkedhashmap offers better performance when iterating through elements since they maintain an ordered entry list, 
while a Hashmap offers better performance when accessing large datasets. Furthermore, when storing objects, 
Linkedhashmap stores objects in key-value pairs, while Hashmap stores them in hash table.

